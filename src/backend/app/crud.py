from sqlalchemy import select
from sqlalchemy.orm import Session

from app.models import User, Workspace, WorkspaceMember
from app.security import hash_password, hash_pin


def get_user_by_email(db: Session, email: str) -> User | None:
    return db.scalar(select(User).where(User.email == email))


def get_user_by_username(db: Session, username: str) -> User | None:
    return db.scalar(select(User).where(User.username == username))


def get_workspace_by_id(db: Session, workspace_id: str) -> Workspace | None:
    return db.get(Workspace, workspace_id)


def _build_user(*, username: str, email: str, password: str, account_type: str) -> User:
    return User(
        username=username,
        email=email,
        password_hash=hash_password(password),
        account_type=account_type,
    )


def create_individual_user(db: Session, *, username: str, email: str, password: str) -> User:
    """Personal (individual) account: plain user, no workspace involved."""
    user = _build_user(username=username, email=email, password=password, account_type="individual")
    db.add(user)
    db.commit()
    db.refresh(user)
    return user


def create_manager_with_workspace(
    db: Session,
    *,
    username: str,
    email: str,
    password: str,
    workspace_name: str,
    workspace_pin: str,
) -> tuple[User, Workspace]:
    """
    Business account, role=manager: creates the user AND a brand-new
    workspace in the same transaction, with the user as its manager.
    workspace_uuid is generated by the DB (public.generate_workspace_id()).
    """
    user = _build_user(username=username, email=email, password=password, account_type="business")
    db.add(user)
    db.flush()  # assigns user.users_uuid without committing yet

    workspace = Workspace(
        workspacename=workspace_name,
        manager_id=user.users_uuid,
        pin_hash=hash_pin(workspace_pin),
    )
    db.add(workspace)

    db.commit()
    db.refresh(user)
    db.refresh(workspace)
    return user, workspace


def create_member_for_workspace(
    db: Session,
    *,
    username: str,
    email: str,
    password: str,
    workspace: Workspace,
) -> User:
    """
    Business account, role=member: creates the user AND attaches them as an
    active member of an already-validated workspace, in the same transaction.
    """
    user = _build_user(username=username, email=email, password=password, account_type="business")
    db.add(user)
    db.flush()  # assigns user.users_uuid without committing yet

    membership = WorkspaceMember(
        user_id=user.users_uuid,
        workspace_id=workspace.workspace_uuid,
        status="active",
    )
    db.add(membership)

    db.commit()
    db.refresh(user)
    return user


def derive_role(db: Session, user: User) -> str:
    manages = db.scalar(select(Workspace.workspace_uuid).where(Workspace.manager_id == user.users_uuid))
    if manages is not None:
        return "manager"

    is_member = db.scalar(
        select(WorkspaceMember.workspace_id).where(
            WorkspaceMember.user_id == user.users_uuid,
            WorkspaceMember.status == "active",
        )
    )
    if is_member is not None:
        return "member"

    return "individual"
